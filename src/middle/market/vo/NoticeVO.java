package middle.market.vo;

public class NoticeVO {
	private String n_cod;
	private String n_title;
	private String n_content;
	private String m_name;
	private String n_startdate;
	
	
	public String getN_code() {
		return n_cod;
	}
	public void setN_code(String n_cod) {
		this.n_cod = n_cod;
	}
	public String getN_title() {
		return n_title;
	}
	public void setN_title(String n_title) {
		this.n_title = n_title;
	}
	public String getN_content() {
		return n_content;
	}
	public void setN_content(String n_content) {
		this.n_content = n_content;
	}
	public String getN_name() {
		return m_name;
	}
	public void setN_name(String m_name) {
		this.m_name = m_name;
	}
	public String getN_date() {
		return n_startdate;
	}
	public void setN_date(String n_startdate) {
		this.n_startdate = n_startdate;
	}
	
	
}
