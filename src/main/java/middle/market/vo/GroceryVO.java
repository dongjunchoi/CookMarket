package middle.market.vo;

public class GroceryVO {
	private String grocery_cod;
	private String g_name;
	private String g_price;
	private String seller_cod;
	private String filename;
	public String getGrocery_cod() {
		return grocery_cod;
	}
	public void setGrocery_cod(String grocery_cod) {
		this.grocery_cod = grocery_cod;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_price() {
		return g_price;
	}
	public void setG_price(String g_price) {
		this.g_price = g_price;
	}
	public String getSeller_cod() {
		return seller_cod;
	}
	public void setSeller_cod(String seller_cod) {
		this.seller_cod = seller_cod;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	

	
}
